class UNet(nn.Module):
    def __init__(self, n_channels, n_classes, bilinear=True):
        super(UNet, self).__init__()
        self.n_channels = n_channels
        self.n_classes = n_classes
        self.bilinear = bilinear

        self.inc = DoubleConv(n_channels, 64)
        self.down1 = Down(256,16)
        self.down2 = Down(128,32)
        self.down3 = Down(64,64)
        self.down4 = Down(32,128)
        self.down5 = Down(16,256)
        self.up1 = Up(32, 128, bilinear)
        self.up2 = Up(64, 64, bilinear)
        self.up3 = Up(128, 128, bilinear)
        self.up4 = Up(256, 256, bilinear)
        self.outc = OutConv(256, n_classes)

    def forward(self, x):
        x1 = self.inc(x)
        x2 = self.down1(x1)
        x3 = self.down2(x2)
        x4 = self.down3(x3)
        x5 = self.down4(x4)
        x = self.up1(x5, x4)
        x = self.up2(x, x3)
        x = self.up3(x, x2)
        x = self.up4(x, x1)
        logits = self.outc(x)
        return logits


